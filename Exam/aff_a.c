#include <unistd.h>

void	ft_aff_a(char *str)
{
	int i;

	i = 0;
	if (str[i] == 'a')
		write(1, "a\n", 2);
	else
		write(1, "a\n", 2);
}

int		main (int argc, char **argv)
{
	if (argc >= 0)
		ft_aff_a(argv[0]);
	return (0);
}
